export function getJobSuggestions(query) {
  if (!query || query.length < 2) return [];
  const q = query.toLowerCase();
  const keywords = q.split(/\s+/);
  return historicalJobs
    .filter(job => {
      const name = job.name.toLowerCase();
      const cat = job.category.toLowerCase();
      return keywords.every(kw => name.includes(kw) || cat.includes(kw));
    })
    .sort((a, b) => b.frequency - a.frequency)
    .slice(0, 6);
}

// ── Helper functions ───────────────────────────────────────
export function getCustomer(id) {
  return customers.find((c) => c.id === id);
}

export function getVehicle(id) {
  return vehicles.find((v) => v.id === id);
}

export function getTech(id) {
  return technicians.find((t) => t.id === id);
}

export function getVehiclesByCustomer(customerId) {
  return vehicles.filter((v) => v.customerId === customerId);
}

export function getROsByStatus(status) {
  return repairOrders.filter((ro) => ro.status === status);
}

export function getROsByTech(techId) {
  return repairOrders.filter((ro) => ro.techId === techId);
}

export function getCustomerForRO(ro) {
  return getCustomer(ro.customerId);
}

export function getVehicleForRO(ro) {
  return getVehicle(ro.vehicleId);
}

export function getTechForRO(ro) {
  return ro.techId ? getTech(ro.techId) : null;
}
